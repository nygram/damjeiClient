package model;

/**
 *
 * @author JavierFernándezDíaz
 */
public class Conductor extends Empleats{
    
    int id_conductor;
    String carnet;

    public Conductor() {
    }

    public Conductor(int id_conductor, String carnet, int id_empresa, String nom, String cognoms, String dni, String categoria) {
        super(id_empresa, nom, cognoms, dni, categoria);
        this.id_conductor = id_conductor;
        this.carnet = carnet;
    }

    public int getId_conductor() {
        return id_conductor;
    }

    public void setId_conductor(int id_conductor) {
        this.id_conductor = id_conductor;
    }

    public String getCarnet() {
        return carnet;
    }

    public void setCarnet(String carnet) {
        this.carnet = carnet;
    }

    public int getId_empresa() {
        return id_empresa;
    }

    public void setId_empresa(int id_empresa) {
        this.id_empresa = id_empresa;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getCognoms() {
        return cognoms;
    }

    public void setCognoms(String cognoms) {
        this.cognoms = cognoms;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }
    
    
}
